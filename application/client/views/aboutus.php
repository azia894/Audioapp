          <h1><b>Book suggestions</b></h1>
          <p>Any well known and well accepted work written or translated into Urdu may be suggested. </p>
          <p>The work may belong to any genre &#8211; typically short stories, society, religion, children, adventure, politics, humour, History, biography, novel, fiction, philosophy etc&#8230; These categories are not exhaustive.</p>
          <p>No book that has obscene or vulgar references shall be included however acclaimed work it may be.</p>
          <p>Any one from the project team, app users or general public can suggest a book. The project management team will prioritise the suggestions depending on the user demand and availability of narrators pool. </p>
          <h2><b>Guidance for the Narrators</b></h2>
          <ul style="list-style-type: circle;">
            <li>Record each chapter in a separate audio file. The file size will therefore depend on the chapter size.</li>
            <li>Please include foreword, preface, introduction of the book/author etc (harf aghaz, pesh lafz, muqaddimah) in your narration</li>
            <li>Please include in a separate audio file list of hard words with meanings in Urdu. </li>
            <li>Towards the end of each audio file include the following tag line [<em>to be confirmed…..!!!Dil ki Awaz Urdu Audiobooks ke liye aap sunrahe thhe ……!!!</em>]</li>
            <li>Do not add any background music or modifications like echo or reverb. Editing to improve voice quality are okay.</li>
            <li>If you come across any obscene or vulgar lines in the book please report to the project manager and hold off narrating the book. These should have been ideally detected at the time of book selection.</li>
            <li>Narrators should be very careful in reading the text accurately without adding or removing anything. Our patrons have advised that narrators should consider their work as an <em>amaanat </em>(a trust) whilst completing the task.</li>
          </ul>
          <p><strong>Technical guidance</strong></p>
          <ul style="list-style-type: circle;">
            <li>Modern smart phones have excellent quality voice recording capability. A simple collar microphone kept at a fixed distance is often all you need.&nbsp;</li>
            <li>Inbuilt mobile phone apps also have the function of moving back a segment and rerecording if there is a mistake.</li>
            <li>Make sure in the settings the recording is stereophonic not monophonic. The default setting is usually stereophonic.</li>
            <li>Using a profession studio facility for recording is not necessary but does have advantages.</li>
            <li>Regardless of the technical set up you are using please be sure to send a 5 minutes sample of the audio to the project manager before starting the main work.</li>
          </ul>
          <p><strong>Essential recording tips by Sajida sahiba</strong></p>
          <ul style="list-style-type: circle;">
            <li>Print the reading material</li>
            <li>Have a cup of water handy</li>
            <li>Keep the device about 1.5 feet away from your mouth to avoid breathing into it, so the voice is not too loud or too quiet</li>
            <li>Be careful with the sounds of ruffling paper</li>
            <li>Pause to take a breath and swallow</li>
            <li>Maintain the enthusiasm. Loss of it will easily show in your voice quality.</li>
            <li>Do take breaks</li>
            <li>Compatibility of the app &#8211; convert to the audio &#8211;</li>
            <li>Be aware of local dialects/ talaffuz / accent</li>
            <li>Rehearse</li>
            <li>Listen to your own voice</li>
            <li>Speak in a natural way, emotions &#8211; sadness happiness</li>
            <li>Feeling the words in the text while you read. Know the story.</li>
            <li>Make it your Dil ki Awaz</li>
            <li>Give natural and human touch</li>
            <li>Become the character you are narrating</li>
            <li>Exercise vocal muscles to relax, breathing exercises on the internet.</li>
            <li>Clear pronunciation</li>
            <li>Gestures are important even though you are only giving your voice. Even if you are alone</li>
            <li>Be confident</li>
            <li>Narrate the story like your own story.</li>
            <li>Early morning voice is most clear</li>
            <li>Look after your voice</li>
          </ul>
