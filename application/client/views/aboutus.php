              <div class="description">
                <p>
                  MAAHIR is an organisation of professionals from Hyderabad, Deccan and its surrounding regions (the
                  erstwhile Nizam state) who have settled in the United Kingdom but retain a strong emotional and
                  cultural bond with Hyderabad. The idea was conceived in discussions over a short period of time by the
                  core group members who realised that migrants from Hyderabad were all highly educated and well
                  established in various professional fields and were spread across Great Britain. For such a group to
                  come together on one platform would be really useful as the combined resources would benefit people in
                  Hyderabad and also new migrants looking to settle in the United Kingdom. The candles that we hope to
                  light will illuminate the path for the generations to follow and represent our attempt at giving back
                  to society what we have gained through a kind providence and through the help that we received from
                  our own benefactors.
                </p>
                <p>
                  To our delight from the first discussions to actually launching the group was but a short step and
                  over the first month since the original idea was floated we had a group of 150 dedicated professionals
                  from Hyderabad with a wide representation of specialties (IT professionals, Doctors, Academics,
                  Finance Managers, Architects, and Immigration Consultants… the list keeps growing).
                </p>
                <p>
                  Over the following month we increased our membership with students now joining the group as the
                  realisation grew that in MAAHIR we had a group of mentors who were eager to share their knowledge and
                  experience to help students with career choices, job interview skills and CV building exercises. Our
                  other objectives are focussed on improving educational opportunities to poor children in Hyderabad and
                  in improving healthcare facilities and also providing increased employment opportunities for women in
                  Hyderabad by ensuring easy acquisition of vocational educational skills.
                </p>
                <p>
                  We are now a fully registered UK charity. We have a good organisational structure in place. Our first
                  project, chosen from a selection of proposals from our members, are closely aligned to our objectives
                  of improving educational opportunities for poor students in Hyderabad. Our second project tackles the
                  dangers of fake news and misinformation as we are all increasingly aware of the importance of the
                  power of social media in shaping attitudes and opinions of people of all ages. In addition to this, as
                  a group of concerned Hyderabadis we are also keen on responding to situations in Hyderabad that
                  require charity funds like the lockdown in the COVID pandemic and more recently the devastating floods
                  that have caused havoc in Hyderabad.
                </p>
                <p>
                  We look forward to exciting and challenging – but also rewarding times ahead. Our talented group
                  members are our strength- and we are confident that we can do a lot of good work in the future,
                  InshaAllah. Please keep visiting this page to learn about new developments.
                </p>
